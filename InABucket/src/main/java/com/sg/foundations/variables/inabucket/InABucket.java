public class InABucket {
    public static void main (String[] args) {
        String walrus;
        double piesEaten;
        float weightOfTeacupPig;
        int grainsOfSand;
        
        walrus = "Ruby Chu";
        piesEaten = 33.2;
        weightOfTeacupPig = (float) 23.44;
        grainsOfSand = 10;
        
        System.out.println("My pet name is: " + walrus);
        System.out.println("The number of pies I ate: " + piesEaten);
        System.out.println("The weight of my pet is: " + weightOfTeacupPig);
        System.out.println("The number of sand: " + grainsOfSand);
    }
}
